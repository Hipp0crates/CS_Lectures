double Factorial(int n) // Факториал
{
    if (n == 1) return 1;
    else return n * Factorial(n - 1);
}
for (int i = 1; i < 21; i++)
{
    Console.WriteLine($"{i}! = {Factorial(i)}");
}

int Fibonacci(int n) // Числа Фибоначи
{
    if (n == 1 || n == 2) return 1;
    else return Fibonacci(n - 1) + Fibonacci(n - 2);
}
for (int i = 1; i < 41; i++)
{
    Console.WriteLine($"f({i}) = {Fibonacci(i)} ");
}