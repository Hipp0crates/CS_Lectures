Console.Clear();

string[,] table = new string[2, 5]; // создание двумерного массива
// String.Empty - инициализация для строк по умолчанию
// table[0 ,0] table[0, 1] table[0, 2] table[0, 3] table[0, 4]
// table[1, 0] table[1, 1] table[1, 2] table[1, 3] table[1, 4]
table[0, 3] = "буква";
table[1, 1] = "слово";
Console.WriteLine("table:");
for (int rows = 0; rows < 2; rows++)
{
    for (int columns = 0; columns < 5; columns++)
    {
        Console.Write("( " + $"{table[rows, columns]}" + " )");
    }
    Console.WriteLine();
}

void PrintArray(int[,] matr) // метод для вывода массива
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            Console.Write("( " + $"{matr[i, j]}" + " )");
        }
        Console.WriteLine();
    }
}

void FillArray(int[,] matr) // // метод для заполнения массива
{
    for (int i = 0; i < matr.GetLength(0); i++)
    {
        for (int j = 0; j < matr.GetLength(1); j++)
        {
            matr[i, j] = new Random().Next(1, 10);
        }
    }
}

int[,] matrix = new int[3, 4]; // new int[GetLength(0), GetLength(1)]
Console.WriteLine("matrix empty:");
PrintArray(matrix);
FillArray(matrix);
Console.WriteLine("matrix filled:");
PrintArray(matrix);

